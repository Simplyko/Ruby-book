# человек берет дом не в рассрочку, а в кредит по ставке 4% годовых на оставшуюся сумму.
# Для каждого года посчитайте, сколько денег нужно заплатить за этот год за использование кредита.

salary_house = 500_000
year = 30
paying = 500_000 / 30
30.times do |i|
  salary_house -= paying
  puts "#{i + 1}-й год: Осталось выплатить #{salary_house} + #{salary_house * 0.04} (4% за использование кредита)"
end

# Задание: посчитайте количество денег (total),
